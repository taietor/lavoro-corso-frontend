/*
Vuoi vedere se hai collegato correttamente il file .js alla tua pagina?
console.log('JavaScript Ok');

Per dichiarare una variabile in JavaScript, 
possiamo utilizzare la parola chiave "var", 
seguita dal nome che vogliamo assegnare alla variabile.

Ad esempio, 
dichiariamo una variabile chiamata "nome" 
e assegniamole il valore "Alice":
*/

var nome = "Alice";

/*
Abbiamo appena creato una variabile di tipo stringa 
e le abbiamo assegnato il valore "Alice".

Le stringhe sono una sequenza di caratteri 
delimitata da virgolette singole o doppie.
*/

/*
Possiamo anche dichiarare variabili numeriche. 
Ad esempio, 
dichiariamo una variabile chiamata "età" 
e assegniamole il valore 25:
*/

var età = 25;

/*
Abbiamo creato una variabile di tipo numerico e le abbiamo assegnato il valore 25.
Ora proviamo a utilizzare le variabili che abbiamo dichiarato stampandole sulla console:
*/

console.log("Il mio nome è", nome);
console.log("Ho", età, "anni.");

/*
Possiamo dichiarare una variabile booleana chiamata "verificato" e assegnarle il valore true:
I booleani rappresentano un valore di verità, che può essere vero (true) o falso (false).
*/

var verificato = true;

// Abbiamo creato una variabile booleana con il valore true.

// Gli array sono collezioni ordinate di elementi.
// Possiamo dichiarare un array chiamato "numeri" che contiene una serie di numeri:

var numeri = [1, 2, 3, 4, 5];

// Abbiamo creato un array con cinque elementi numerici.

// Possiamo accedere agli elementi dell'array utilizzando l'indice.
// Ad esempio, per accedere al secondo elemento dell'array "numeri":

console.log("Il secondo numero nell'array è:", numeri[1]); // Stampa il valore "2" sulla console.


// Gli oggetti sono collezioni di proprietà, dove ogni proprietà ha un nome e un valore associato.
// Possiamo dichiarare un oggetto chiamato "persona" con alcune proprietà:

var persona = {
  nome: "Alice",
  età: 25,
  professione: "developer"
};

// Abbiamo creato un oggetto con tre proprietà: nome, età e professione.

// Possiamo accedere alle proprietà dell'oggetto utilizzando la notazione del punto o la notazione delle parentesi quadrate.
// Ad esempio, per accedere alla proprietà "nome" dell'oggetto "persona":

console.log("Il nome della persona è:", persona.nome); // Stampa il valore "Alice" sulla console.


